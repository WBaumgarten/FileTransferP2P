    public void connect() {
        String username = txfUsername.getText().trim();
        String serverIP = txfServerIP.getText().trim();
        int serverPort = (int) spnPort.getValue();
        if (username.equals("")) {
            txfActivity.setText("Please enter a username.");
        } else if (serverIP.equals("")) {
            txfActivity.setText("Please enter a server address.");
        } else {
            client = new Client(serverIP, serverPort, username);
            if (!client.start()) {
                //txaClientOut.setText(" *** Error starting client *** \n");
                txfActivity.setText("Error starting client.");
            } else if (client.readStartMessage()) {
                client.startListeningFromServer();
                txfActivity.setText("Successfully connected.");
                btnConnect.setEnabled(false);
                txfServerIP.setEditable(false);
                txfUsername.setEditable(false);
                spnPort.setEnabled(false);
                btnAddFile.setEnabled(true);
                btnDownload.setEnabled(false);
                btnSearch.setEnabled(true);
                btnLogout.setEnabled(true);
                //btnUpPause.setEnabled(true);
                //btnDownPause.setEnabled(true);
            } else {
                txfActivity.setText("This username is not available.");
                txfUsername.setText("");
            }

        }
    }

