    private void btnAddFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFileActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(null);
        File f = fileChooser.getSelectedFile();

        try {
            String filePath = f.getAbsolutePath();
            String[] split = filePath.split("/");
            String fileName = split[split.length - 1];
            client.addFile(new FileEntry(filePath, fileName));

            txaFileList.append(filePath + "\n");
            System.out.println(filePath);
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnAddFileActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        client.clearResults();
        String fileSearch = txfSearch.getText().trim();
        Message m = new Message(Message.SEARCH);
        m.setUsername(client.getUsername());
        m.setMesssage(fileSearch);
        client.sendMessage(m);
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownloadActionPerformed
        String[] split = ((String) cmbSearchResults.getSelectedItem()).split(": ");
        Message m = new Message(Message.REQUESTDOWNLOAD);
        m.setMesssage(split[1]);
        m.setUsername(split[0]);
        m.setUsername2(client.getUsername());
        double randomKey = Math.random();
        m.setRandomKey(randomKey);
        client.setCurRandomKey(randomKey);
        System.out.println("Random key: " + randomKey);
        client.generateKeyPairs();
        m.setPublicKey(client.getPublicKey());
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        fileChooser.setDialogTitle("Choose a folder where to save the file.");
        fileChooser.setAcceptAllFileFilterUsed(false);
        String filePath = "./";
        progressBarDown.setValue(0);
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File f = fileChooser.getSelectedFile();
            filePath = f.getAbsolutePath();
            client.sendMessage(m);
            client.setDestFilePath(filePath + "/" + m.getMessage());
        } else {
            System.out.println("No Selection ");
        }
    }//GEN-LAST:event_btnDownloadActionPerformed

